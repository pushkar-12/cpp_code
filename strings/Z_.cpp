ll powmod(ll base,ll pow)
{
   ll res=1;
   while(pow)
   {
         if (pow%2!=0)  res=(res*base)%MOD;
         base=(base*base)%MOD;
         pow/=2;
   }
   return res;
}

vector<ll> zfunc(string s) {
    ll n = (ll) s.length();
    vector<ll> z(n);
    for (ll i = 1, l = 0, r = 0; i < n; ++i) {
        if (i <= r)
            z[i] = min (r - i + 1, z[i - l]);
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }
    return z;
}

void find_match(string &text,string &pat)
{
	string qs=pat+text;
	vi z=zfunc(qs);
	for(int i=pat.length();i<qs.length();i++)
	{

		if(z[i]>=pat.length())
			cout<<"pattern found at: "<<i-pat.length()<<"\n";
	}
}

